FISCAL_TYPES = ["本決算", "第1四半期", "第2四半期", "第3四半期"]
ANNOUNCE_TIMES = ["引け後", "寄付前", "15:00", "未定"]

# 各サンプル銘柄の決算予定日を「今日からのオフセット(日数)」で指定する。
# 今日/明日/今週/来週/1ヶ月内 がバランスよく含まれるように配置。
SCHEDULE_OFFSETS = [
    0, 0, 1, 1, 2, 3, 4, 6, 7, 8, 9, 10,
    11, 13, 14, 15, 17, 18, 20, 22, 24, 26, 28, 29,
]


def seed(reference_date=None):
    """サンプルデータを投入する（既存データがある場合はスキップ）。

    reference_date: 決算予定日の基準日。省略時は実行日。
    戻り値: 投入した銘柄数（既に投入済みなら 0）。
    """
    ref = reference_date or datetime.date.today()
    now = datetime.datetime.now().replace(microsecond=0).isoformat()

    conn = db.connect()
    try:
        existing = conn.execute("SELECT COUNT(*) AS c FROM stocks").fetchone()["c"]
        if existing:
            return 0

        for code, name, market, sector, cap, listing in SAMPLE_STOCKS:
            conn.execute(
                "INSERT INTO stocks (code, name, market, sector, market_cap, listing_type, updated_at)"
                " VALUES (?,?,?,?,?,?,?)",
                (code, name, market, sector, cap, listing, now),
            )

        for i, (stock, offset) in enumerate(zip(SAMPLE_STOCKS, SCHEDULE_OFFSETS)):
            code = stock[0]
            date = (ref + datetime.timedelta(days=offset)).isoformat()
            fiscal = FISCAL_TYPES[i % len(FISCAL_TYPES)]
            time = ANNOUNCE_TIMES[i % len(ANNOUNCE_TIMES)]
            conn.execute(
                "INSERT INTO earnings_schedule (code, announce_date, fiscal_type, announce_time, source, updated_at)"
                " VALUES (?,?,?,?,?,?)",
                (code, date, fiscal, time, "サンプルデータ", now),
            )
        conn.commit()
        return len(SAMPLE_STOCKS)
    finally:
        conn.close()
